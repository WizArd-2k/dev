void Fmoyenne(unsigned char *img){
	a = 0;
	b = 1;
	c = 2;
	for(a = 0 , b = 1 , c =2; a < 430 && b < 431 && c < 432; a +=3 , b+=3 , c+=3){
		Valeur1= img[a];
		Valeur2= img[b];
		Valeur3= img[c];
		moyenne = (Valeur1+Valeur2+Valeur3)/3;
		if (a%36 ==0 && a!=0){
			printf("\n");
		}
		if (moyenne <=127){
			printf("█");
		}
		else{
			printf(" ");
		}
	}
	printf("\n");
}
